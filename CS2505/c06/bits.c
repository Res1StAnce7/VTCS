/* 
 * isNonZero - Check whether x is nonzero using
 *              the legal operators except !
 *   Examples: isNonZero(3) = 1, isNonZero(0) = 0
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4 
 *   Logic: After certain operation, we want to make sure the final result is 
 *   00000000000000000000000000000001 or 00000000000000000000000000000000.
 *   For the first, three lines we get the max integer value of 32 bits.
 *   The sign of x does not matter. if x is postive, x + 2147483647(max int) will 
 *   cause it to become a negative max integer to get a leading 1 at the first bit.
 *   Similarly, if x is negaitve, x | (x + 2147483647) will make sure 
 *   there is a leading 1 at the first bit. Then, we shift the number 31 bits 
 *   to the left to make sure the last bit is 1 since (size of int is 4 bytes which is 32 bits). 
 *   Next, we use | operator to get 11111111111111111111111111111111. In the end,
 *   we can use & with 1(00000000000000000000000000000001) to get the final result.
 */
int isNonZero(int x) {
   int a = ~0;
   int b = 1 << 31;
   int c = ~(a & b);
   return ((x | (x + c)) >> 31) & 1;
}

