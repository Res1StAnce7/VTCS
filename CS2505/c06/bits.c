/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 *   Logic: First, we need to shift c by the right number of bits to the left
 *   (if n == 1 shift by 8, if n == 2 shift by 16, if n == 3 shift by 24).
 *   255 << (n << 3) will makes the lowest 8 bits of c zero ans shift it to left
 *   with the same value as the the previous step does. Then we use & to operate
 *   the result with the original x to zero out the right bits of x. In the end, 
 *   we use | to combine the shifted c value with x to replace these bits.
 */
int replaceByte(int x, int n, int c) 
{
   return (x & ~(255 << (n << 3))) | (c << (n << 3));
}
